    public void mostrarEstadistica() {
        EmpleadoPT empleadoPT = new EmpleadoPT();
        EmpleadoFT empleadoFT = new EmpleadoFT();

        double pagarFT = 0;
        double pagarPT = 0;
        double hTurno1 = 0;
        double hTurno2 = 0;

        Vector empleadosPT;
        empleadosPT = empleadoPT.obtenerListado();

        for (int i = 0; i < empleadosPT.size(); i++) {
            EmpleadoPT empleado = (EmpleadoPT) empleadosPT.elementAt(i);
            pagarPT += empleado.getSueldo();
            if (empleado.getTurno()) {
                hTurno1 += empleado.getCantidad_horas();
            } else {
                hTurno2 += empleado.getCantidad_horas();
            }
        }

        Vector empleadosFT;
        empleadosFT = empleadoFT.obtenerListado();

        for (int i = 0; i < empleadosFT.size(); i++) {
            EmpleadoFT empleado = (EmpleadoFT) empleadosFT.elementAt(i);
            pagarFT += empleado.getSueldo();
        }

    }

