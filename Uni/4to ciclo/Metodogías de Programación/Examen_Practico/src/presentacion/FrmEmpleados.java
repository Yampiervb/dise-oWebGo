    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String dni, paterno, materno, nombres;
        int antiguedad;
        int horas_extras;
        double tarifa_horas_extras;
        double sueldo_base;
        dni = this.txtDni.getText();
        paterno = this.txtPaterno.getText();
        materno = this.txtMaterno.getText();
        nombres = this.txtNombres.getText();

        antiguedad = Integer.parseInt(this.txtAños.getText());
        horas_extras = Integer.parseInt(this.txtHorasextra.getText());

        if (horas_extras > 10) {
            JOptionPane.showMessageDialog(null, "Limite de 10 horas extras");//Validacion maximo 10 horas extras.
            return;
        } else if (horas_extras < 0) {
            JOptionPane.showMessageDialog(null, "Vuelva a intentar");
            return;
        }

        tarifa_horas_extras = Double.parseDouble(this.txtTarifaxhoraextra.getText());
        sueldo_base = Double.parseDouble(this.txtSueldoBase.getText());

        EmpleadoFT empFT = new EmpleadoFT(dni, paterno, materno, nombres, antiguedad, horas_extras, tarifa_horas_extras, sueldo_base);

        empFT.agregarEmpleado();
        empFT.calcularPesoP();
        this.mostrarEmpleadosFT();
        this.mostrarEstadistica();


    }//GEN-LAST:event_btnGuardarActionPerformed

